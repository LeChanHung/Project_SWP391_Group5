    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        DAO1 dao = new DAO1();
        List<news> newList = dao.getAllNews();
        int size = newList.size();
        int page, numberpage = 5;
        int num=(size%5==0?(size/5):((size/5)+1));
        String xpage = request.getParameter("page");
        if (xpage == null) {
            page = 1;

        } else {
            page = Integer.parseInt(xpage);
        }
        int start, end;
        start = (page - 1) * numberpage;
        end = Math.min(page * numberpage, size);

        List<news> list = dao.getListbByPage(newList, start, end);
        request.setAttribute("page", page);
        request.setAttribute("num", num);
        request.setAttribute("newList", list);
        request.getRequestDispatcher("addNews.jsp").forward(request, response);
        
    }

    /**
     * Handles the HTTP <code>POST</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doPost(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
       
            String title = request.getParameter("title");
            String content = request.getParameter("content");
            String date = request.getParameter("date");
            String img = request.getParameter("img");

            DAO1 dao = new DAO1();
            dao.insertNews(title, content, date, img);
            response.sendRedirect("manage_news");
        

        
    }

