    /**
     * Handles the HTTP <code>GET</code> method.
     *
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        String search = request.getParameter("search") == null ? "" : request.getParameter("search");
        search = search.trim();
        DAO1 dao = new DAO1();
        int totalrows = dao.countFeedbackBySearch(search);
//        int totalproduct = productDao.getNumberProduct();
        int numberPage = 1;
        if (totalrows % 4 == 0) {
            numberPage = totalrows / 4;
        }
        if (totalrows % 4 != 0) {
            numberPage = (totalrows / 4) + 1;
        }
        if (totalrows < 4 )  {
            numberPage = 1;
        }
        
        int index;
        String currentPage = request.getParameter("index");
        if (currentPage == null) {
            index = 1;
        } else {
            index = Integer.parseInt(currentPage);
        }
        request.setAttribute("datas", dao.getFeedbackBySearchAndPaging(search, index));
        request.setAttribute("numberPage", numberPage);
        request.getRequestDispatcher("listFeedback.jsp").forward(request, response);

    }

